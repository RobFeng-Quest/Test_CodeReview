namespace code_example
{
    public class SimpleArithmetic
    {
        public int Addition(int x, int y)
        {
            return x + y;
        }

        public int Subtraction(int x, int y)
        {
            return x - y;
        }

        public decimal Division(int x, int y)
        {
            return x / y;
        }
    }
}
